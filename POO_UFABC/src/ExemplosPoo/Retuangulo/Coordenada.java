package ExemplosPoo.Retuangulo;

public class Coordenada {
    private final double coordenadaX;
    private final double coordenadaY;

    Coordenada(double coordenadaX, double coordenadaY) {
        this.coordenadaX = coordenadaX;
        this.coordenadaY = coordenadaY;
    }

    double getCoordenadaX() {
        return coordenadaX;
    }

    double getCoordenadaY() {
        return coordenadaY;
    }
}
